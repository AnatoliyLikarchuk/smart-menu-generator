/**
 * Функция получения перевода блюда
 * @param {string} dishName - название блюда на английском
 * @returns {string} название на русском или оригинальное
 */
export function translateDishName(dishName) {
  return DISH_TRANSLATIONS[dishName] || dishName;
}

/**
 * Функция получения перевода ингредиента
 * @param {string} ingredient - ингредиент на английском
 * @returns {string} ингредиент на русском или оригинальный
 */
export function translateIngredient(ingredient) {
  if (!ingredient) return '';
  
  const cleanIngredient = ingredient.toLowerCase().trim();
  return INGREDIENT_TRANSLATIONS[cleanIngredient] || ingredient;
}

/**
 * Функция получения перевода категории
 * @param {string} category - категория на английском
 * @returns {string} категория на русском или оригинальная
 */
export function translateCategory(category) {
  return CATEGORY_TRANSLATIONS[category] || category;
}

